function [Y] = exam2_5species(nsteps, y0)

if nargin <2;
    Y(1:5,1) = 0.001;
else
    Y(1:5,1) = y0;
end

for i=2:nsteps
    t = i-1;
    Y(1,i) = Y(1,t)*(4-4*Y(1,t)-2*Y(2,t)-0.4*Y(3,t));
    Y(2,i) = Y(2,t)*(3.1-0.31*Y(1,t)-3.1*Y(2,t)-0.93*Y(3,t));
    Y(3,i) = Y(3,t)*(2.12 + 0.636*Y(1,t) + 0.636*Y(2,t)-2.12*Y(3,t));
    Y(4,i) = Y(4,t)*(3.8-0.111*Y(1,t)-0.011*Y(2,t) + 0.131*Y(3,t)-3.8*Y(4,t));
    Y(5,i) = Y(5,t)*(4.1-0.082*Y(1,t)-0.111*Y(2,t)-0.125*Y(3,t)-4.1*Y(5,t));
end